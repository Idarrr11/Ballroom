                     <p class="excert">
                        Setiap tahun, kampus Politeknik Nest menyambut kedatangan mahasiswa baru dengan program PKKMB (Pengenalan Kehidupan Kampus Mahasiswa Baru). PKKMB adalah acara yang dirancang untuk membantu mahasiswa baru beradaptasi dengan lingkungan kampus yang baru dan mempersiapkan mereka untuk kehidupan perkuliahan. 
                     </p>
                     <p>
                        Tahun ini, PKKMB diadakan selama lima hari. Acara ini diisi dengan berbagai kegiatan menarik, seperti perkenalan dengan dosen dan staf, kegiatan ice breaking, pertunjukan seni dan kemah makrab (malam keakraban). Mahasiswa baru juga diajak untuk mengunjungi berbagai fasilitas kampus, seperti perpustakaan, laboratorium, dan aula serbaguna.
                     </p>
                     <p>
                        Salah satu kegiatan yang paling dinantikan oleh mahasiswa baru adalah perkenalan dengan senior. Senior adalah mahasiswa yang telah berpengalaman dan dapat memberikan tips dan saran kepada mahasiswa baru tentang kehidupan perkuliahan. Mahasiswa baru juga diajak untuk bergabung dengan berbagai organisasi kemahasiswaan, seperti himpunan mahasiswa jurusan, UKM (Unit Kegiatan Mahasiswa), dan senat mahasiswa.
                     </p>
                     <p>
                        Melalui PKKMB, mahasiswa baru diharapkan dapat lebih mengenal kampus dan teman-teman seangkatannya. Mereka juga diharapkan dapat mengembangkan soft skill dan kepemimpinan mereka. PKKMB adalah awal dari perjalanan mereka di dunia perkuliahan, sebuah perjalanan yang penuh dengan tantangan dan kesenangan.
                     </p>

                     
